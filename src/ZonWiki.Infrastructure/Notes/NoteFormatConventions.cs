namespace ZonWiki.Infrastructure.Notes;

/// <summary>
/// 全站「AI 產出／整理筆記」共用的排版慣例文字。
///
/// 目的：把「每個標題摺疊、句子換行」等排版規則**集中在一處**，讓排版(Reformat)、
/// 美化(Beautify)、精煉成筆記(Refine) 等系統提示共同引用，避免各自複製、日後漏改
/// （對應使用者「各功能傳送給 AI 的 prompt 都要改、不要漏掉」的要求）。
/// MCP（TypeScript）因跨語言無法共用此常數，另於 mcp/src/index.ts 維護對應文字。
/// </summary>
public static class NoteFormatConventions
{
    /// <summary>
    /// 排版慣例規則（可直接嵌入各系統提示）。核心兩條：①每個標題各自摺疊、②每句話換行。
    /// </summary>
    public const string Rules =
        "排版慣例（請嚴格遵守）：\n" +
        "1.【每個標題都摺疊】把每一個標題（原本的 #／##／###）都改寫成各自的「摺疊區塊」：\n" +
        "   - 用一行「:::toggle 標題文字」起頭（標題文字**不要保留 # 符號**），把「該標題之下、到下一個同級或更高級標題之前」的所有內容放進去，最後用單獨一行「:::」收尾。\n" +
        "   - 標題有階層時要**巢狀**：上層標題的摺疊區塊「裡面」直接放下層標題的摺疊區塊；收尾的「:::」由最內層往最外層、每一層各一行。\n" +
        "   - 摺疊冒號一律用三個「:::」（靠層數自然巢狀，不要用四個以上）。想讓某塊預設展開改用「:::toggle-open」。\n" +
        "   - 範例（H1 內含 H2）：\n" +
        "     :::toggle 大標題\n" +
        "     這是導言。  \n" +
        "     :::toggle 小標題\n" +
        "     這是小節內容。  \n" +
        "     :::\n" +
        "     :::\n" +
        "2.【句子換行】每句話結束（句號「。」、問號「？」、驚嘆號「！」之後）就換行，避免單行橫向過長；\n" +
        "   每個因此換行的**行尾要補「兩個半形空格」**再換行，Markdown 才會真的斷行渲染。\n" +
        "   （表格的儲存格內、程式碼區塊內不套用此換行規則，以免破壞表格與程式碼。）\n" +
        "3. 無序列表用「- 」、有序列表用「1. 2. 3.」，巢狀用適當縮排。\n" +
        "4. 行內程式碼用反引號，程式碼區塊標註語言。\n" +
        "5. 強調：粗體 **文字**、斜體 *文字*。\n" +
        "6. 表格用標準 Markdown 表格。\n" +
        "7. 連結／圖片用 [文字](url) 與 ![alt](url)。\n" +
        "8. 分隔線用 ---、引用用 >。\n" +
        "9. 移除多餘空行（最多連續兩行），中英文／數字之間加空格，檔尾保留單一換行。";

    /// <summary>
    /// 結尾強制指示：只輸出內容本身、禁止任何開場白／說明／圍欄。
    /// （解決模型常在最前面多加「我來排版」「I'll reformat…」開場白、卻違反「不要加說明文字」的問題。）
    /// </summary>
    public const string OutputOnly =
        "只輸出處理後的 Markdown 全文。**第一個字元起就必須是筆記內容本身**：" +
        "嚴禁任何開場白、結語或說明文字（例如不得出現「我來排版」「以下是排版結果」「I'll reformat」這類句子），" +
        "也不要用 ``` 圍欄把整篇包起來。";
}
