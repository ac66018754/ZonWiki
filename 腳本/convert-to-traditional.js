function convertContent(content) {
  let result = content;
  // 按長度排序（長的先替換，避免嵌套問題）
  const sortedKeys = Object.keys(charMap).sort((a, b) => b.length - a.length);
  
  for (const key of sortedKeys) {
    const regex = new RegExp(key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
    result = result.replace(regex, charMap[key]);
  }
  return result;
}

function getAllFiles(dir) {
  const results = [];
  const files = fs.readdirSync(dir);
  
  for (const file of files) {
    // 跳過 .git 和 node_modules
    if (file === '.git' || file === 'node_modules' || file === '.github') continue;
    
    const fullPath = path.join(dir, file);
    const stat = fs.statSync(fullPath);
    
    if (stat.isDirectory()) {
      results.push(...getAllFiles(fullPath));
    } else if (file.endsWith('.md') || file.endsWith('.ps1')) {
      results.push(fullPath);
    }
  }
  return results;
}

