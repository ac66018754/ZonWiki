let changedCount = 0;

for (const file of files) {
  try {
    const originalContent = fs.readFileSync(file, 'utf8');
    const newContent = convertContent(originalContent);
    
    if (originalContent !== newContent) {
      fs.writeFileSync(file, newContent, 'utf8');
      changedCount++;
      console.log(`✅ 已轉換：${file}`);
    }
  } catch (error) {
    console.error(`❌ 錯誤 ${file}: ${error.message}`);
  }
}

console.log('');
console.log('✨ 轉換完成！');
console.log(`修改檔案數：${changedCount}\n`);
