$files = Get-ChildItem -Path $TargetDir -Recurse -Include "*.md", "*.ps1" | Where-Object { $_.FullName -notmatch '\.git' }

Write-Host "找到 $($files.Count) 個檔案需要檢查" -ForegroundColor Yellow
Write-Host ""

$changedCount = 0
$totalReplacements = 0

foreach ($file in $files) {
    # 讀取檔案內容
    $content = Get-Content -Path $file.FullName -Raw -Encoding UTF8
    $originalContent = $content
    
    # 執行替換
    foreach ($key in $characterMap.Keys) {
        $content = $content -replace [regex]::Escape($key), $characterMap[$key]
    }
    
    # 如果內容有變更，寫回檔案
    if ($content -ne $originalContent) {
        $replacementCount = (($originalContent | Measure-Object -Character).Characters - ($content | Measure-Object -Character).Characters)
        Set-Content -Path $file.FullName -Value $content -Encoding UTF8 -NoNewline
        
        $changedCount++
        Write-Host "✅ 已轉換：$($file.FullName)" -ForegroundColor Green
        
        # 計算具體的替換次數
        $diff = 0
        foreach ($key in $characterMap.Keys) {
            $count = (($originalContent | Select-String -Pattern [regex]::Escape($key) -AllMatches).Matches.Count)
            $diff += $count
        }
        if ($diff -gt 0) {
            $totalReplacements += $diff
        }
    }
}

